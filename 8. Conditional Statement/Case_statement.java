import java.util.*;

public class Case_statement {
    public static void main(String args[]) {
        int a = 1;
        switch (a) {
            case 1:
                System.out.println("Monday");
                break;
            case 2:
                System.out.println("Tuesday");
                break;

            default:
            System.out.println("Opps!!!");
                break;
        }

    }
}
